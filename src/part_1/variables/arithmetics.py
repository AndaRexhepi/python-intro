# The datatype of an operand usually determines the data type of the result:
# if two integers are added together, the result will also be an integer.
# If a floating point number is subtracted from another floating point number,
# the result is a floating point number.
# In fact, if a single one of the operands in an expression is a floating point number,
# the result will also be a floating point number, regardless of the other operands.

# if you use / you will receive a floating point number
print(9/8)
# if you use // you will receive an integer
print(9//8)

